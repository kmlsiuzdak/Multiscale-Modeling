package com.kmlsiuzdak;

public class data_value {
    private final int x;
    private final int y;
    private final int value;

    public data_value(int x, int y, int value) {
        this.x = x;
        this.y = y;
        this.value = value;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getValue() {
        return value;
    }

}
